/*
Operadores relacionales o de comparacion
Mayor que       a>b
Menor que       a<b
Mayor igual que a>=b
Menor igual que a<=b
Igual que a==b
distinto que    a!=b
Operador estricto ===
Operador diferente estricto !==
*/

let numero1 = 22;
let numero2 = 15;

console.log("Número 1 es mayor a Número 2 : " + String(numero1>numero2));
console.log("Número 1 es menor a Número 2 : " + String(numero1<numero2));
console.log("Número 1 es mayor igual a Número 2 : " + String(numero1>=numero2));
console.log("Número 1 es menor igual a Número 2 : " + String(numero1<=numero2));
console.log("Número 1 es igual a Número 2 : " + String(numero1==numero2));
console.log("Número 1 es distinto a Número 2 : " + String(numero1!=numero2));
console.log("Número 1 es estrictamente igual a Número 2 : " + String(numero1===numero2));
console.log("Número 1 es distinto estrictamente a Número 2 : " + String(numero1!==numero2));